create table if not exists public."user" (
  id uuid primary key references auth.users(id) on delete cascade,
  full_name text not null,
  role text not null check (role in ('admin', 'player')),
  player_id text unique,
  is_active_player boolean not null default true
);

create table if not exists public.location_master (
  id bigint generated by default as identity primary key,
  facility_name text not null,
  address text,
  map_url text,
  phone_number text,
  email text,
  remark text,
  is_active boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.schedule (
  id bigint generated by default as identity primary key,
  schedule_date date not null,
  start_time time,
  end_time time,
  title text not null,
  location_id bigint references public.location_master(id) on delete set null,
  description text,
  created_by uuid not null references public."user"(id) on delete restrict,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.attendance (
  id bigint generated by default as identity primary key,
  schedule_id bigint not null references public.schedule(id) on delete cascade,
  user_id uuid not null references public."user"(id) on delete cascade,
  attendance_date date not null,
  status text not null check (status in ('present', 'absent', 'late')),
  note text,
  recorded_at timestamptz not null default now(),
  unique (schedule_id, user_id)
);

create index if not exists idx_schedule_schedule_date
on public.schedule(schedule_date);

create index if not exists idx_schedule_location_id
on public.schedule(location_id);

create index if not exists idx_attendance_attendance_date
on public.attendance(attendance_date);

create index if not exists idx_attendance_user_id
on public.attendance(user_id);

alter table public."user" enable row level security;
alter table public.location_master enable row level security;
alter table public.schedule enable row level security;
alter table public.attendance enable row level security;

create policy "Users can read own profile"
on public."user"
for select
to authenticated
using (auth.uid() = id);

create policy "Authenticated users can read users"
on public."user"
for select
to authenticated
using (true);

create policy "Authenticated users can read locations"
on public.location_master
for select
to authenticated
using (true);

create policy "Admin can manage locations"
on public.location_master
for all
to authenticated
using (
  exists (
    select 1
    from public."user" as me
    where me.id = auth.uid()
      and me.role = 'admin'
  )
)
with check (
  exists (
    select 1
    from public."user" as me
    where me.id = auth.uid()
      and me.role = 'admin'
  )
);

